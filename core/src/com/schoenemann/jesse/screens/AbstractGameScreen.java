public abstract class AbstractGameScreen extends ScreenAdapter implements Screen {

    protected ReversiGame myGame;
    protected Stage stage;
    protected OrthographicCamera camera;
    protected Viewport viewport;


    public AbstractGameScreen(ReversiGame game){
        myGame = game;
        camera = new OrthographicCamera(Assets.SCREEN_WIDTH, Assets.SCREEN_HEIGHT);
        viewport = new FitViewport(Assets.SCREEN_WIDTH, Assets.SCREEN_HEIGHT, camera);
        stage = new Stage(viewport);
    }

    @Override
    public void resize (int width, int height) {
        stage.getViewport().update(width, height, true);
    }

    @Override
    public void dispose() {
        stage.dispose();
    }




}
